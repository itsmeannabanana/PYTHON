# Casting Tuples and Lists
# Two Examples

# Example 1 (Convert Tuple to List)
courses = ("BSIT", "BSCS", "BLIS", "BSMT", "BSBA")
courses = list (courses)
print (courses)

# Example 1 (Convert List to Tuple)
courses = ("BSIT", "BSCS", "BLIS", "BSMT", "BSBA")
courses = tuple (courses)
print (courses)

